package 반복문;


public class 반복문2 {

	public static void main(String[] args) {
		int i = 0; //시작값
		
		while (i < 10) { // 조건식
			System.out.println("밥먹을 시간이에요");
			
			i++; //증감식
		}
		System.out.println(i);
	}
}

